#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	j = 1;
	if (ac > 1)
	{
		while (j < ac)
		{
			k = ac - j;
			i = 0;
			while (av[k][i])
			{
				write(1, &av[k][i], 1);
				i++;
			}
			j++;
			write(1, "\n", 1);
		}
	}
	return (0);
}
