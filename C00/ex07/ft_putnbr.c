#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	res;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	res = 0;
	while (nb >= 10)
	{
		ft_putnbr(nb / 10);
		nb = nb % 10;
	}
	if (nb < 10)
	{
		res = nb + '0';
		write(1, &res, 1);
	}
}
